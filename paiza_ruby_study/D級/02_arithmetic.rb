# 問題1: 足し算
#
# 問題文：
# 2つの整数 a, b が空白区切りで入力されます。
# a + b の結果を出力してください。
#
# 入力例：
# 3 5
#
# 出力例：
# 8

# 解答例1
a, b = gets.split.map(&:to_i)
puts a + b

