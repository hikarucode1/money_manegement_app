# ==================================================
# 問題3: 挨拶
#
# 問題文：
# 名前が入力されるので、"Hello [名前]" の形式で出力してください。
#
# 入力例：
# paiza
#
# 出力例：
# Hello paiza

# 解答例3
# name = gets.chomp
# puts "Hello #{name}"

# 別解（文字列結合）
# name = gets.chomp
# puts "Hello " + name

# ==================================================
# 問題4: 複数行出力
#
# 問題文：
# 以下の3行を出力してください。
# 1行目: Hello
# 2行目: paiza
# 3行目: World
#
# 入力：
# なし
#
# 出力：
# Hello
# paiza
# World

# 解答例4
# puts "Hello"
# puts "paiza"
# puts "World"

# 別解（配列を使用）
# ["Hello", "paiza", "World"].each { |line| puts line }

# ==================================================
# 学習ポイント
#
# 1. puts は改行付きで出力する
# 2. print は改行なしで出力する
# 3. gets.chomp で1行入力を取得（改行文字を除去）
# 4. #{変数名} で文字列内に変数を埋め込める（文字列補間）
# 5. + で文字列を結合できる
# 6. 配列のeachメソッドで繰り返し処理ができる